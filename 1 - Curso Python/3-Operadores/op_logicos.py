#AND

resultado1 = True & True #Devolvera True
resultado2 = False & True #Devolvera False
resultado3 = True & False #Devolvera False
resultado4 = False & False #Devolvera True

#OR

resultado5 = True | True #Devolvera True
resultado6 = False | True #Devolvera True
resultado7 = True | False #Devolvera True
resultado8 = False | False #Devolvera False

#NOT

resultado9 = not True #Devolvera False
resultado10 = not False #Devolvera True

